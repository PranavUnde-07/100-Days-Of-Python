# ARITHMATICS OPERATIONS ARE USED FOR CALCULATIONS 

'''

1. ADDITION : +
2. SUBTRACTION :  -
3. MULTIPLICATION :  *
4. DIVISION :  /
5. MODULUS :  %
6. EXPONENT :  **
7. FLOOR DIVISION :  //

'''
print(6+5) # 11       addition 
print(6-5) # 1        subtraction
print(6*5) # 30       multiplication
print(6/5) # 1.2      division
print(6%5) # 1        modulus 
print(6**5) # 7776    exponent ( raise to sign )
print(6//5) # 1       floor division ( returns the integer value of the quotient )
