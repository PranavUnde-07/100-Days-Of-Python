# We can use /n for breaking a line and start new line 

print("Hey iam a good\nBoy/Girl in this class")










# We can use /' or /" if we're using more then one number of double quotes in syntax 


print("Hey iam a good\n\"Boy/Girl\" in this class")

'''OUTPUT : Hey iam a good
"Boy/Girl" in this class'''









# WE CAN SEPARATE INBETWEEN TERMS BY USING    sep="~"   SYNTAX


print(4,5,6,7,4,6,7,6,4, sep="-")

'''OUTPUT: 4-5-6-7-4-6-7-6-4 '''









# WE CAN WRITE SYNTAX   end="Done"  FOR ENDING ANYTHING IN OUR PRINT STATEMENT


print(4,5,6,7,4,6,7,6,4,end="\nDone")

'''OUTPUT: 4 5 6 7 4 6 7 6 4
Done'''